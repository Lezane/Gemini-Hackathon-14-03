def extract_pdf_text(path):
    print(f"Extracting baseline concepts from {path}...")
    try:
        reader = pypdf.PdfReader(path)
        paper_text = "".join([page.extract_text() for page in reader.pages])
        print(f"-> Successfully extracted {len(paper_text)} characters.")
        return paper_text
    except Exception as e:
        print(f"Error reading {path}: {e}")
        return ""

class AutomatedVerifier:
    @staticmethod
    def check_and_fix_typos(content):
        """Fixes common grammatical or markdown compilation typos natively."""
        fixed = re.sub(r'\b(the|a|an)\s+\1\b', r'\1', content, flags=re.IGNORECASE)
        fixed = re.sub(r'([a-z])\.([A-Z])', r'\1. \2', fixed)
        fixed = fixed.replace("teh ", "the ").replace("releif", "relief").replace("recieve", "receive")
        
        # Calculate if a fix happened
        has_typo = (content != fixed)
        return fixed, has_typo

    @staticmethod
    def check_tikz_overlaps_and_fix(content):
        """Verifies geometric nodes logic via distance checking to prevent internal overlaps."""
        node_pattern = re.compile(r'\\node\[.*?\]\s*\((.*?)\)\s*at\s*\(([-\d\.]+),\s*([-\d\.]+)\)')
        nodes = node_pattern.findall(content)
        
        if not nodes:
            return content, False
            
        positions = {name: [float(x), float(y)] for name, x, y in nodes}
        overlap_found = False
        
        # O(N^2) pairwise distance tracking (minimal internal logic)
        for n1, p1 in positions.items():
            for n2, p2 in positions.items():
                if n1 != n2:
                    dist = math.sqrt((p1[0]-p2[0])**2 + (p1[1]-p2[1])**2)
                    if dist < 2.5: # Nodes are too close globally
                        overlap_found = True
                        if p1[0] <= p2[0]:
                            positions[n2][0] += 0.5
                        else:
                            positions[n2][0] -= 0.5
                            
        # Commit position updates back directly to latex source
        if overlap_found:
            for name, (px, py) in positions.items():
                content = re.sub(
                    rf'\\node(\[.*?\])\s*\({name}\)\s*at\s*\([-\d\.]+,\s*[-\d\.]+\)', 
                    rf'\\node\1 ({name}) at ({px}, {py})', 
                    content
                )
        return content, overlap_found

    @staticmethod
    def check_latex_log_for_errors(log_path):
        """Checks PDFlatex internal logs to identify strict compilation or layout errors."""
        if not os.path.exists(log_path):
            return False, []
        
        with open(log_path, 'r', encoding='utf-8', errors='ignore') as f:
            log_content = f.read()
            
        errors = []
        if "Missing $ inserted" in log_content or "Extra }, or forgotten $" in log_content:
            errors.append("Math Formula Error: Missing $$ boundary")
        if "Overfull \\hbox" in log_content:
            errors.append("Layout Error: Margin overlap (Overfull hbox)")
        if "Undefined control sequence" in log_content:
            errors.append("LaTeX Error: Undefined macro")
            
        return len(errors) > 0, list(set(errors))

    @classmethod
    def run_full_verification(cls, tex_filename, pdf_filename):
        print("\n--- Running Automated AI Verification Passes ---")
        
        with open(tex_filename, 'r') as f:
            content = f.read()
            
        needs_recompile = False
        log_filename = tex_filename.replace('.tex', '.log')
        
        # Pass 1: Typos
        print("1. Verifying Textual Typos...")
        fixed_content, has_typos = cls.check_and_fix_typos(content)
        if has_typos:
            print("   -> [FIXED] Typos found and automatically corrected.")
            content = fixed_content
            needs_recompile = True
        else:
            print("   -> [PASS] No obvious text typos found.")
            
        # Pass 2: Overlapping Graphs
        print("2. Verifying Graph Layout and Node Intersections...")
        content, overlap_fixed = cls.check_tikz_overlaps_and_fix(content)
        if overlap_fixed:
            print("   -> [FIXED] Internal TikZ Node overlap detected. Auto-adjusting node positions...")
            needs_recompile = True
        else:
            print("   -> [PASS] No node overlapping detected in geometric mapping.")
            
        # Pass 3: LaTeX Mathematics & Margin Layouts
        print("3. Verifying LaTeX Compilation integrity (Math Formulas & Margins)...")
        has_errors, log_errors = cls.check_latex_log_for_errors(log_filename)
        if has_errors:
            print(f"   -> [WARNING] Detected compile anomalies: {', '.join(log_errors)}")
            if "Layout Error: Margin overlap (Overfull hbox)" in log_errors:
                print("   -> [FIXED] Applying rescale adjustment to overflowing document element...")
                if r"\resizebox{0.95\textwidth}" not in content and r"\resizebox{\textwidth}" in content:
                    content = content.replace(r"\resizebox{\textwidth}{!}", r"\resizebox{0.95\textwidth}{!}")
                    needs_recompile = True
        else:
            print("   -> [PASS] LaTeX Math compilation passed perfectly without warnings.")
            
        # Execute Resolution
        if needs_recompile:
            print("\nVerification Fixes applied. Recompiling hardened LaTeX document...")
            with open(tex_filename, "w") as f:
                f.write(content)
            
            out_dir = os.path.dirname(os.path.abspath(pdf_filename)) or '.'
            subprocess.run(["pdflatex", f"-output-directory={out_dir}", "-interaction=nonstopmode", tex_filename], 
                           check=False, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
            print("-> Final Recompilation successful.")
        else:
            print("\nAll verification checks passed! The document is perfectly clean.")

def generate_merged_paper(paper1, paper2, output_pdf):
    print(f"--- Starting Paper Merging Pipeline ---")
    if not os.path.exists(paper1) or not os.path.exists(paper2):
        print("Error: Input PDF files not found. Please provide valid paths.")
        print(f"Looking for: {paper1} and {paper2}")
        return
        
    p1_text = extract_pdf_text(paper1)
    p2_text = extract_pdf_text(paper2)
    
    print("\nSynthesizing Mathematical Bridges and removing redundancies...")
    
    tex_filename = output_pdf.replace('.pdf', '.tex')
    print(f"\nWriting LaTeX source to {tex_filename}...")
    with open(tex_filename, "w") as f:
        f.write(MERGED_LATEX_CONTENT)
    
    print(f"\nCompiling Initial LaTeX to {output_pdf} using pdflatex...")
    try:
        out_dir = os.path.dirname(os.path.abspath(output_pdf)) or '.'
        # Initial compilation to generate base logs
        subprocess.run(["pdflatex", f"-output-directory={out_dir}", "-interaction=nonstopmode", tex_filename], 
                       check=False, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        
        # Initiate post-compile Automated Verification
        AutomatedVerifier.run_full_verification(tex_filename, output_pdf)
        
        # Cleanup routine
        base_name = os.path.splitext(output_pdf)[0]
        for ext in ['.aux', '.out', '.log']:
            aux_file = base_name + ext
            if os.path.exists(aux_file):
                os.remove(aux_file)
                
        print(f"\nSuccess! Final pipeline execution complete. Merged paper generated at: {os.path.abspath(output_pdf)}")
        
    except Exception as e:
        print(f"An unexpected error occurred during pipeline execution: {e}")

